package com.Wifi;

public class WifiLevel {
	int value;

	public WifiLevel(int level) {
		super();
		this.value = level;
	}

	/**
	 * @return the level
	 */
	public int getValue() {
		return value;
	}

	/**
	 * @param level the level to set
	 */
	public void setValue(int level) {
		this.value = level;
	}
}
